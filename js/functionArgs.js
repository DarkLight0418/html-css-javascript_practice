// sumAll 함수는 전달된 모든 인자들을 더해서 그 합계를 반환합니다.
function sumAll(...args) {
    // sum이라는 변수를 0으로 초기화합니다. 이 변수는 최종 합계를 저장하는 데 사용됩니다.
    let sum = 0;

    // for...of 문은 배열 또는 반복 가능한(iterable) 객체의 각 요소를 순회할 때 사용합니다.
    // 여기서는 args 배열(전달된 모든 인자들)을 순회하며 각 요소를 arg라는 변수에 담아 사용합니다.
    for (let arg of args) {
        // 현재 순회 중인 값을 sum에 더합니다.
        sum += arg;
    }

    // 모든 값을 더한 결과를 반환합니다.
    return sum;
}

// sumAll 함수에 1부터 5까지의 숫자를 전달하고, 그 합계를 c에 저장합니다.
c = sumAll(1, 2, 3, 4, 5);

// 결과를 콘솔에 출력합니다. 예상 출력: 15
console.log(c);
